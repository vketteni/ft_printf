#include "util.h"

int	ft_is_format_specifier(char c)
{
	if (c == 'd' || c == 'i' || c == 's' || c == 'x' || c == 'X' || c == 'u'
		|| c == 'p' || c == 'c' || c == '%')
		return (1);
	return (0);
}
